public class Exam {
    //Attributes
    private String examDesc;

    //Constructor
    public Exam(String examDesc) {
        this.examDesc = examDesc;
    }

    //Getters
    public String getExamDesc() {
        return examDesc;
    }

    //Setters
    public void setExamDesc(String examDesc) {
        this.examDesc = examDesc;
    }
}
